# Hardcoded variables
item_name = "Laptop"     # String
quantity = 2             # Integer
price = 499.99           # Float
in_stock = True          # Boolean

# Print formatted receipt using commas
print("Item:", item_name, 
      "Qty:", quantity, 
      "Price:", price, 
      "Available:", in_stock)

# Calculate total cost
total_cost = quantity * price

# Print total cost
print("Total Cost:", total_cost)
